class FromUnicodeMapping(dict):
    """
    Dead simple dict class to use with unicode/str `translate` method.

    Map non-ascii unicode characters in a unicode string to ascii, for
    normalization purposes. E.g.: mystr.translate(FromUnicodeMapping())
    """

    def __missing__(self, key):
        char = self.get(key)
        if char is not None:
            return char
        char = CHARACTER_MAP.get(unichr(key), key)
        self[key] = char
        return char


def map_from_unicode(text):
    return text.translate(FromUnicodeMapping())

